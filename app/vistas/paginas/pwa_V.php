            <p class="p_1">PWA - Progresive web app</p>
            <p class="p_8">¿Que es una PWA?</p>
            <p>Se trata de una nueva tecnologia para desarrollar aplicaciones móviles accesibles desde un navegador web, sin necesidad de descargar todo el paquete de una aplicación movil. Son multiplaformas porque estan desarrolladas para funcionar en las plataforma Android, Apple, Huawey y Web.</p>
            <!-- <p class="p_8">Diferencias entre APP y PWA?</p> -->
            <p class="p_8">Caracteristicas</p>
            <ul class="" style="width: 80%; margin:auto;">
                <li>Una PWA puede instalarse en el dispositivo movil para acceder a ella desde su propio icono, como si de una App nativa se tratara.</li>
                <li>Gracias a los avances de los navegadores, una PWA se ve y se comporta exactamente igual que una aplicación nativa en cuanto a la visualización, animaciones, gestos, etc.</li>
                <li>Ocupan un minimo de espacio en memoria del movil, solo para presentar el icono de acceso en la pantalla del dispositivo.</li>
                <li>Las actualizaciones son transparentes al usuario, ya que estas se realizan y son cargadas al servidor sin que el usuario tenga que descargarse la nueva versión.</li>
                <li>No es necesario acudir a las APP Stores para descargar la aplicación.</li>
            </ul>
            <p class="p_8">Como instalar una PWA</p>
            <p>Al acceder al sitio web una notificación de permiso para instalación aparecerá, acepte la instalación y automaticamente se instalará el icono de la aplicación en el escritorio de su movil.</p>
            <p>En los casos que no acepte la instalacion y decida instalarla más adelante, es necesario borrar la memoria cahe del dispositivo para que la notificación de instalación vuelva a aparecer al entrar al sitio web.</p>
